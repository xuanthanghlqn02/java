public class HinhTamGiac {
    public static void chuVi() {
        int canhThuNhat = 5;
        int canhThuHai = 4;
        int canhThuBa = 3;
        int chuVi = canhThuNhat + canhThuHai + canhThuBa;
        System.out.println("Chu vi hình tam giác là: " + chuVi);
    }

    public static void dienTich() {
        int a = 10;
        int ha = 5;
        double dienTich = 0.5 * a * ha;
        System.out.println("Diện tích hình tam giác là: " + dienTich);
    }
}