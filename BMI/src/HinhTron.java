public class HinhTron {
    public static double chuVi() {
        int banKinh = 5;
        double soPi = 3.14;
        double chuVi = 2 * banKinh * soPi;
        return chuVi;
    }

    public static double dienTich() {
        int banKinh = 5;
        double soPi = 3.14;
        double dienTich = banKinh * banKinh * soPi;
        return dienTich;
    }
}
