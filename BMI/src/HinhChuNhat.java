public class HinhChuNhat {
    public static int chuVi() {
        int chieuDai = 10;
        int chieuRong = 5;
        int chuVi = (chieuDai + chieuRong) * 2;
        return chuVi;
    }

    public static int dienTich() {
        int chieuDai = 10;
        int chieuRong = 5;
        int dienTich = chieuDai * chieuRong;
        return dienTich;
    }
}
